public class Sight {
    private String name;
    private int animalId;
  
    public Sight(String name, int animalId) {
      this.name = name;
      this.animalId = animalId;
    }
  
    public String getName(){
      return name;
    }
  
    public int getAnimalId(){
      return animalId;
    }
  
  }